public final class ValueSetValue extends Taggable {

    private final String key;
    private final String value;

    public ValueSetValue(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 73 * hash + Objects.hashCode(this.key);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final ValueSetValue other = (ValueSetValue) obj;
        return Objects.equals(this.key, other.key);
    }

    @Override
    public ValueSetValue clone() {
        ValueSetValue vs = new ValueSetValue(this.key, this.value);
        return vs;
    }

}
